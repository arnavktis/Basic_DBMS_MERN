function buttonClicked1() {
    var divToToggle = document.querySelector(".inputstyle");
    if (divToToggle.style.display === "none" || divToToggle.style.display === "") {
        divToToggle.style.display = "block";
    }
    else {
        divToToggle.style.display = "none";
    }
}

